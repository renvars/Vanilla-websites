const ADDRESS = "0xBE73474EBE5C44Cd3f30B69e134c5f9C9940CfDb";
(async() => {
    document.getElementById("mint_button").onclick = async() => {
        if (window.ethereum) {
            await window.ethereum.request({ method: 'eth_requestAccounts' });
            window.web3 = new Web3(window.ethereum);
            web3 = new Web3(window.ethereum);

            var accounts = await web3.eth.getAccounts();
            account = accounts[0];

            contract = new web3.eth.Contract(ABI, ADDRESS);

            // const newItem = document.createElement('p');
            // newItem.innerHTML = account
            // var button = document.getElementById("connect")
            // button.parentNode.replaceChild(newItem, button)
        } else {
            window.addEventListener('ethereum#initialized', handleEthereum, {
                once: true,
            });

            // If the event is not dispatched by the end of the timeout,
            // the user probably doesn't have MetaMask installed.
            setTimeout(handleEthereum, 3000); // 3 seconds
        }


        if (contract) {
            document.querySelector("#mint_button").innerHTML = "Mint"
            document.querySelector("#mint_amount").innerHTML = "1"
            document.querySelector("#mint_amount").style.fontSize = "4rem"
            document.querySelector("#mint_button").onclick = () => {
                var mint_amnt = parseInt(document.querySelector("#mint_amount").innerHTML)
                console.log("Minting " + mint_amnt)
                contract.methods.mint(mint_amnt + 1).send({ from: account, value: String(mint_amnt * 50000000000000000) })
            }

        }

    }
})();

